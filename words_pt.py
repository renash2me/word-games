        'UFAS', 'MELA', 'PALA', 'TETA', 'REGIA', 'TRENO', 'MANA',
        # --- 6+ letras ruins ---
        'BATALA', 'DOCTOR', 'FAROLS', 'HISTOR', 'HUMORA', 'IRMANOS',
        'SOCEGAR', 'AUGMENTAR', 'CARUJA', 'POLITOS', 'TOPEIRA', 'ARRAIADO',
        'AGORINHA', 'BASTANTEIRO', 'FOLEADORES', 'CANINABAIXA', 'CASTIGADAS',
