        'TOBOGAN', 'BASQUETEIRO', 'CHARMOSINHO', 'AMASSADINHO', 'EDGARIN',
        'WAHAB', 'DAYLEWIS', 'BALLADAS', 'DANCARE', 'FALAVAM', 'CONTARAM',
        'ACREDITAVA', 'JANTARA', 'VALERA', 'DEVERIAM', 'MALTAR', 'VAGAIS',
        'TROPECA', 'APARTADO', 'REVEZA', 'RETOMA', 'INFORMA', 'DEFESORES',
        'BARCAROLA', 'BEATIFICO', 'CALCULIAS', 'FIGUREIRA', 'DOMICI',
        'ADOLESCENTE', 'APERFEICOAR', 'ARREMESSO', 'ARRUMACAO', 'ASSOLADOR',
        # --- Palavras muito longas (>10) que escaparam ---
        'EXTRAORDINARIO', 'BRUTALIDADE', 'CARPINTEIRO', 'ATENTAMENTE',
        'AUTORITARIO', 'AMABILIDADE', 'ALMISCARADO', 'ALARGAMENTO',
        'AGRICULTURA', 'AVENTUREIRO', 'CRIMINALIDADE', 'CORDIALIDADE',
        'COMPLETAMENTE', 'COMPOSITORES', 'COMPARTILHAR', 'CONTINUACAO',
        'CATASTROFES', 'CAVALHEIROS', 'CHURRASCARIA', 'CONHECIMENTO',
        'CARNAVALESCO', 'BONDOSAMENTE', 'BRIGADEIROS', 'CACHOEIRAS',
        'CAFEICULTOR', 'ADIVINHACAO', 'ANIVERSARIO', 'ACAMPAMENTO',
